public class Circle extends Shape {

    private double radius;

    public void setRadius(double radius) {
        this.radius = radius;
    }

    private String color;

    public Circle () {
        radius= 1.0;
        color = "red";
    }

    public double getRadius() {
        return radius;
    }

    public double getArea() {
        return radius;
    }



    
}
